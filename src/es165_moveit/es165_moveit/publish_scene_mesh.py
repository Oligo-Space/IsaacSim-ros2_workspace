import rclpy
from rclpy.node import Node
from visualization_msgs.msg import Marker
from geometry_msgs.msg import Pose
import os
from ament_index_python.packages import get_package_share_directory


class SceneMeshPublisher(Node):
    """
    Publishes the scene mesh STL file as a Marker for visualization in RViz
    Generated by Cursor
    """
    def __init__(self):
        super().__init__('scene_mesh_publisher')
        
        # Get the path to the scene mesh
        package_share_dir = get_package_share_directory('es165_moveit')
        mesh_path = os.path.join(package_share_dir, 'usd', 'scene_mesh.stl')
        
        if not os.path.exists(mesh_path):
            self.get_logger().error(f"Scene mesh not found at: {mesh_path}")
            return
        
        self.mesh_publisher = self.create_publisher(Marker, '/scene_mesh', 10)
        
        # Publish the mesh marker at 1 Hz
        self.create_timer(1.0, self.publish_mesh)
        
        self.get_logger().info(f"Publishing scene mesh from: {mesh_path}")
    
    def publish_mesh(self):
        marker = Marker()
        marker.header.frame_id = "base_link"
        marker.header.stamp = self.get_clock().now().to_msg()
        marker.ns = "scene_mesh"
        marker.id = 0
        marker.type = Marker.MESH_RESOURCE
        marker.action = Marker.ADD
        
        # Set the mesh resource path
        package_share_dir = get_package_share_directory('es165_moveit')
        mesh_path = os.path.join(package_share_dir, 'usd', 'scene_mesh.stl')
        marker.mesh_resource = f"file://{mesh_path}"
        marker.mesh_use_embedded_materials = True
        
        # Set pose (identity - mesh is already in correct frame)
        marker.pose = Pose()
        marker.pose.orientation.w = 1.0
        
        # Set scale (1:1 scale)
        marker.scale.x = 1.0
        marker.scale.y = 1.0
        marker.scale.z = 1.0
        
        # Set color (semi-transparent gray)
        marker.color.r = 0.7
        marker.color.g = 0.7
        marker.color.b = 0.7
        marker.color.a = 0.5
        
        self.mesh_publisher.publish(marker)


def main(args=None):
    rclpy.init(args=args)
    node = SceneMeshPublisher()
    try:
        rclpy.spin(node)
    except KeyboardInterrupt:
        pass
    finally:
        node.destroy_node()
        rclpy.shutdown()


if __name__ == '__main__':
    main()
